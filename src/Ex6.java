
public class Ex6 {

	public static void main(String[] args) {
		byte value1 = 127;
		System.out.println((int)value1);
		
		int value2 = 32768;
		System.out.println((short)value2);
		
		float value3 = 95.7f;
		System.out.println((double)value3);
		
		double value4 = 179.8;
		System.out.println((float)value4);
		
		
		/*데이터의 크기는
		 * 실수 >정수 >문자 이런식임
		 * 업캐스팅 : 값이 작은 범위에서 큰 범위로의 형 변환(형 변환 타입 생략가능함)
		 * 다운캐스팅 : 값이 큰 범위에서 작은 범위로의 형 변환 (형 변환 타입 생략 불가능)*/
		
	}

}
